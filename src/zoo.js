function animalsByIds(...ids) {
  return animals.filter((animal) => ids.includes(animal.id));
}

function animalsOlderThan(animal, age) {
  return animals.find((elAnimal) => elAnimal.name === animal)
    .residents.every((res) => (res.age >= age));
}

